    def __init__(self, _id, _type, date, until, modID, modTag, reason, punishment):
        self.id         = _id
        self.type       = _type
        self.date       = date
        self.until      = until
        self.modID      = modID
        self.modTag     = modTag
        self.reason     = reason
        self.punishment = punishment